    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String N = txtNombre.getText();
        int E = Integer.parseInt(txtEdad.getText());
        double Est, P;
        Est = Double.parseDouble(txtEstatura.getText());
        P = Double.parseDouble(txtPeso.getText());

        try {

            if (!txtNombre.getText().trim().equals("")
                    || !txtEdad.getText().trim().equals("")
                    || !txtEstatura.getText().trim().equals("")
                    || !txtPeso.getText().trim().equals("")) 
            {
                clsJug.agregar(N, E, Est, P);
                JOptionPane.showMessageDialog(this, "Registrado.");
                limpiar();
                refrescar();
            }
            else {
                JOptionPane.showMessageDialog(this, "No debe dejar datos sin llenar");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Algunos valores no coinciden con el formato requerido");
            limpiar();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

